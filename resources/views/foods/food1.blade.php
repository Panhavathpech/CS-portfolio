            <h2 class="mb-3">A super soft and custard-y japanese tamagoyaki inspired french toast is 100 percent the best way to start the day.</h2>
            <p>This Japanese tamagoyaki-inspired french toast is soft and custardy on the inside and crisp on the outside. The perfect combination of sweet and savory!</p>
            <p>
              <img src="{{ asset('images/image_6.jpg')}}" alt="" class="img-fluid">
            </p>
            <p>I am a true lover of french toast and all it’s varieties. I’m pretty sure I’ve talked about my dream french toast restaurant before – the one that would kind of be like IHOP but for french toast instead of pancakes. Needless to say, I’m a french toast fanatic. I like to try french toasts wherever we travel to. Usually they tend to be just like the classic french toast: bread dipped in eggs and milk and fried, but sometimes I encounter a new style of french toast.

My most recent french toast obsession is Japanese french toast! It’s soft and custardy and very similar to classic french toast while being completely different.</p>
            <h2 class="mb-3 mt-5">What is Japanese Style French Toast</h2>
            <p>Japanese style french toast is almost just like regular – it uses bread and eggs and is fried, but somehow the sum of its parts ends up being different than the french toast you know and love.

First off, Japanese french toast starts off with shokupan: super fluffy milk bread. The crusts are almost always cut off and the super soft and fluffy bread soaks up liquid like a sponge. They also tend to soak their bread in a more custard-y egg mix with eggs, milk or cream, and sugar. The bread is soaked for a long time so that the bread is completely saturated. There’s a famous french toast at a popular Tokyo hotel that soaks their bread overnight which results in the most tender, custard-y french toast ever. The bread is then cooked low and slow ensuring that the insides are creamy and cooked through while the outsides are just the slightest bit golden. The eggs get puffy and fluffy and it is just SO GOOD. Lots of the cafes in Tokyo do Japanese style french toast as well.</p>
            <p>
              <img src="{{ asset('images/image_4.jpg')}}" alt="" class="img-fluid">
            </p>
            <h2 class="mb-3 mt-5">What is Tamagoyaki</h2>
            <p>Tamagoyaki (literally grilled egg) is one of my favorite sushi pieces. It’s the one that is bright yellow and looks like egg (duh!) and is sweet and savory and really good. Essentially it’s a rolled omelette made with with eggs seasoned with soy sauce and mirin (a kind of sweet Japanese very low alcohol content rice wine). I love making tamagoyaki and I love french toast so I thought I’d combine the two into the ultimate Japanese french toast, mixing sweet and savory.</p>
            <h2 class="mb-3 mt-5">Reasons Why You Should Make Japanese Tamagoyaki Inspired French Toast</h2>
            <p>You love french toast. <br>
              You love tamagoyaki. <br>
              You have some mirin in the pantry and want to use it up. <br>
              You have some fluffy bread hanging around. <br>
              You love custard-y french toast and hate french toast that is dry in the middle.<br>
              You want to try a new french toast technique!</p>
            <h2 class="mb-3 mt-5">How to Make Tamagoyaki French Toast</h2>
            <p>
                <ol>
                  <li>Cut the bread into thick slices and trim the crusts off the bread.</li>
                  <li>Create a savory egg custard mix by whisking together the eggs, cream, sugar, soy, and mirin. For an extra smooth french toast, strain the mixture. </li>
                  <li>Soak the bread for minimum 1 hour on both sides, flipping carefully. If you can go longer, do it – you want the bread to be completely saturated. </li>
                  <li>Heat up a bit of oil in a non-stick pan over very low heat. Let the piece of bread drain a little bit then place in the pan and cook, covered, on very low heat for about 8-10 minutes. Use an offset spatula and take a peek, the bottom should be golden and the top should be puffy. Flip, cover, and continue to cook until golden and puffed up. Serve on a plate with powdered sugar, syrup, and whipped cream, if desired.</li>
                </ol>
                  </p>
            <h2 class="mb-3 mt-5">Tips and Tricks</h2>      
            <p>If you can, use shokupan, milk bread, or bread you buy at the Asian grocery store. It has the fluffiness and ability to absorb the custard. Best is if you buy unsliced bread so you can cut it into thick slices for extra fluffy french toast. </p>
            <p>Strain the egg mixture before soaking the bread. It’s an extra step that might seem a bit extraneous but if you want french toast that’s a uniform golden yellow and doesn’t have bits of egg whites speckling the surface, this is what you want to do. </p>
            <p>Soak the bread as long as you can, evenly on both sides to ensure that the custard saturates and soaks all the way through. This is not a french toast recipe that has dry insides. You can even soak overnight. This of course depends on your bread though. If it’s too fresh, it might just fall apart on you, so make sure you keep an eye on it. </p>
            <p>Cook on extremely low heat and take your time. The key to cooking the custard all the way through is low heat, covered. The low heat, combined with the fact that the pan is covered will make your toast puff up gently and ensures softness and jiggles. </p>
            <p>If you love fluffy Japanese pancakes, you’ll love this fluffy Japanese french toast! I hope you give it a try and if you do, tag @iamafoodblog on instagram, I’d love to see! </p>
