            <h2 class="mb-3">Everyone wants to bake sourdough but what if you can’t eat a giant loaf of sourdough all by yourself? Small batch sourdough starter and a small batch sourdough loaf are here to save you!</h2>
            <p>It seems like everyone is either sweets baking away their blues or caffeinating at a high rate while they’re stuck at home during Covid. But small batch baking treats and coffee aside, I think the biggest thing that everyone is doing right now is baking sourdough!</p>
            <p>
              <img src="{{ asset('images/image_6.jpg')}}" alt="" class="img-fluid">
            </p>
            <p>Are you a fan of sourdough? A long time ago I didn’t like it. That was back in the day when people’s sourdough loaves were literally sour. Artisan bread has come a long way from then and now sourdough is complex, rich, and seriously good. I’m pretty sure anyone who has had a really good sourdough has thought about making sourdough at home. After all, all you need is flour and water.</p>
            <p>If you’ve been to the grocery store lately you’ve probably noticed lots of empty shelves. Paper products and cleaning supplies are missing. Eggs are hit or miss, flour is hard to come by and there is not yeast. What’s a new bread maker to do? Make your own! Gather wild yeast and start a sourdough starter. Yeast is everywhere, you just need to harness it.</p>
            <h2 class="mb-3">What is Sourdough?</h2>
            <p>Sourdough is loose term that refers to bread that’s been made with a wild yeast starter rather than commercial yeast. Contrary to the name, not all sourdough tastes sour at all. Sourdough starter can be used to make all kinds of yeasted breads: sourdough cinnamon buns, sourdough, babka, essentially anything that uses yeast can be made with sourdough.</p>
            <h2 class="mb-3">What is Yeast and Why is it Wild?</h2>
            <p>Yeast are the little beasties that brings your bread to life! Yeast is what makes breads light and fluffy. Basically it eats the sugars in flour and releases carbon dioxide, which makes bread rise.</p>
            <p>There’s wild yeast everywhere around us. It’s in the air, in flours, in trees, on fruit, it’s everywhere.</p>
            <h2 class="mb-3">What Can I Bake With My Yeast/Sourdough Starter?</h2>
            <p>You can use sourdough starter/yeast to bake anything! Sourdough bread obviously, but also things like pizza crust, focaccia, rustic loves, sandwich bread, baguettes, pretzels, doughnuts, and the list goes on. Anything yeasted is a go.</p>
            <h2 class="mb-3">What About Sourdough Discard?</h2>
            <p>When you feed your sourdough (more on that later) you need to take away some of the sourdough mixture otherwise you’ll end up with too much sourdough. The amount you take away is called “discard.”</p>
            <h2 class="mb-3">What Can I Make With Sourdough Discard?</h2>
            <p>When you have a sourdough starter there’s always going to be discard otherwise you’ll end up with a giant vat of living breathing sourdough starter that will eat you out of house and home. Even with a small sourdough starter, you’ll have discard. But, the good news is that there are tones of things you can make with it:</p>
            <p>pancakes, waffles, English muffins, crumpets, popovers/Yorkshire puddings, cake, banana bread, quick breads, crackers, muffins, corn bread, naan.</p>
            <h2 class="mb-3">Why You Should Make a Small Sourdough Starter</h2>
            <p>Right now flour is a hot commodity. Because there will inevitably be sourdough discard the best thing you can do if you want to make sourdough is make a small sourdough starter. Having a smaller starter means less discard and less flour to feed. A small starter will be more than enough for a home baker to bake multiple loaves of bread because you can use your starter to create a levain, which is an offshoot of your starter. The best part though is that you won’t need a huge amount of flour at the beginning. It’s a low investment scaled down starter.</p>
            <h2 class="mb-3">How to Make a Small Batch of Sourdough Starter</h2>
            <h2 class="mb-3">What You Need</h2>
            <p>
                <ol>
                    <li>Flour – It’s easier to start a sourdough starter with freshly milled flour but you can most definitely do it with AP flour. If you have whole wheat or rye flour on hand, a 50/50 mix of flour is best.</li>
                    <li>Water – Room temperature filtered water or tap water that’s left out overnight so that any chlorine in the water disappeared.</li>
                    <li>A container – I like to use a small, tall, straight sided glass container so I can see exactly how much my starter grows and when it needs to be feed. You’ll need a jar with a lid but don’t put the lid on tightly, so gases can escape.</li>
                    <li>Kitchen Scale – Technically you could eyeball your starter and use tablespoons but if you’re going to be baking sourdough you’re probably going to want to invest in a kitchen scale. A kitchen scale will give you accuracy and help you bake consistent loves.</li>
                    <li>Rubber spatula – This isn’t technically a “need” but it makes it really easy to mix your starter and scrape down the sides of your container.</li>
                </ol>           
            </p>
