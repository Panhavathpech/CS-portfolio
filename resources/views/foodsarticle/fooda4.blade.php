            <h2 class="mb-3">Fish Soup: Healthy and Wholesome</h2>
            <p>Somlar Machu Ktiss (Cambodian Hot and Sour Coconut Fish Soup) is almost as complex as Cambodian curry, so it doesn’t make regular weekly appearances on our dinner plate, but when it does, I always jump in joy and anticipation. So healthy, explosively flavourful, complex and beguilingly delicious, I can’t help but go for a second or a third round. Needless to say, there are never any leftovers when my mother prepares this somlar machu ktiss. </p>
            <p>
              <img src="{{ asset('images/image_6.jpg')}}" alt="" class="img-fluid">
            </p>
            <p>Over the years, I’ve come to realize that I am quite lucky. While my friends struggle with the newest diet fads, juice cleanses or paleo diet, I am regularly spending my time as usual; visiting the local farmer’s market for fresh ingredients. Cambodian diet is full of vegetables, fermented foods and a minimal amount of lean proteins, so I have always been lean and healthy. My mother prepared wholesome meals from scratch so artificial ingredients and processed foods were a rarity in our household. Even now, in my own household, processed or artificial food is not something I approve of, although somehow it does manage to sneak in from time to time (thank you, Jose, you sneaky devil!).</p>
            <p>Although Cambodian food is very healthy, to get that complex taste and vibrant colour, fresh ingredients are pounded to a pulp with a mortar and pestle to make the spice (kroeung). Preparing it fresh can be daunting for first-timers, especially when looking at the ingredient list. Hopefully, for the many curious foodies, this wouldn’t be a hard obstacle to overcome as the ingredients can be easily found at an Asian supermarket.</p>
            <h2 class="mb-3">Finding Ingredients for this Coconut Fish Soup</h2>
            <p>The most difficult ingredient to find is Galangal, a rhizome, used heavily in Indonesian, Thai and Cambodian cuisine. Galangal may look like an upscale version of ginger at first glance and one may assume it is just another variety of ginger, but they are actually quite different. Ginger has a sharp pungent taste whereas galangal’s pungency is more muted and packed with a more medicinal, piney notes. The skin of galangal is tighter and much lighter in colour, almost translucent, and its flesh is quite dense –almost as hard as wood. For this recipe, galangal cannot be replaced with ginger —it is acting as a freshener to cut out the fishy taste of the catfish. </p>
            <p>Similar to ginger, galangal has numerous medicinal uses and health benefits. Like turmeric, it has many anti-inflammatory qualities and is therefore very helpful in treating arthritis,  rheumatoid arthritis, as well as inflammation caused by ulcers. Feeling a little nausea? Chew on a little hunk of galangal to help ease symptoms of motion and morning sickness. Galangal also improves circulation, maintain healthy digestion, and helps alleviate diarrhea.</p>
            <p>Galangal is available fresh at most Asian supermarkets but you can also find it preserved in brine which is also just as good as fresh, although with a much longer shelve life (and much easier to cut!). If you are using whole fresh galangal, you might find that your knife will literally just slide over it if it’s not sharp enough, in which case, just soak it in hot water before attempting to slice it again.</p>
            <p>There seems to be a turmeric fad lately, so this is an ingredient that shouldn’t be hard to find. If it is hard to find fresh turmeric, turmeric powder can be used instead. There’s no preference really, it’s just there for colouring and health purposes. It doesn’t affect the flavour as much as galangal, whose fresh whole form (or brined form) is a necessity to cut the fishy taste of the catfish which will be used for this recipe.</p>
            <p>For this Cambodian Hot and Sour Coconut Fish Soup recipe, we will be using Thai eggplants, long beans and catfish. Thai eggplant is a very common food ingredient in Cambodia. Usually the size of a golf ball or smaller, mostly round, they range in colour from all green to green with stripes and a few streaks of purple or yellow. Unlike the larger oblong eggplants, Thai eggplants are crunchy but tender on the outside, with a mild flavour and a tiny hint of delicate bitterness on the inside. Which is why they are mostly enjoyed raw (and unskinned) in the Cambodian household, usually served with a dipping sauce made from fish such as tirk kroeung or prahok ktiss which is a dipping sauce made from pork and coconut milk. </p>
            <p>When eaten raw, Thai eggplants are better in its immature state, so look for smaller/younger ones that are glossy and colourful. When eaten raw, their scent is also very mild and neutral, however, when cooked, they develop an earthy aromatic scent. When overly mature, the flesh is more bitter and stringy, especially their seeds. Yes, they have seed. And due to this, they are actually classified as fruits even though many consider them as a vegetable (me included).</p>
            <p>Mainstream supermarkets or farmer’s market don’t usually have them so the best way to get Thai eggplants would be in an Asian grocery store. If you’re able to buy them individually, make sure to look for the freshest ones —the stem should be green rather than brown and firmly attached to the eggplants. They should be firm with dark green tones, avoid ones that have too many streaks of yellow.</p>
            <p>I usually do not like catfish, but in this somlar, the delicately mild flavour of catfish whose taste is almost similar to that of a sweet whitefish works well to balance the heat and flavours of the soup. One great thing I like about catfish is that it is naturally low in sodium which is great for those on a low-salt diet or those that trying to reduce or avoid high blood pressure. Although I do love my salmon, adding catfish to my diet not only adds variety to my meal. it also a great low-fat and healthy alternative.</p>
            <h2 class="mb-3">How to clean catfish for Cambodian Hot and Sour Coconut Fish Soup</h2>
            <p>Place the whole catfish (innards removed) into a bowl. Add 2 tablespoon of vinegar and 1 tablespoon salt into the bowl and fill it with water. Scrap the skin of the catfish to remove any dirt and slime while in this solution. Once clean, remove from the solution, wash it with water and cut it into chunks (see picture below).</p>
            <p>This Cambodian Hot and Sour Coconut Fish Soup is one of those healthy, hearty meal that everyone in the family will love. My recipe is a bit on the spicy side, but you can turn down the heat if you prefer, but personally, I love the spiciness. It keeps me warm during those cold winter months and cools me down in the summer (from all that sweat!). Either way, give this somlar machu ktiss a try today!</p>
